/*
--- Difference between Enums and Classes
- An enum can, just like a class, have attributes and methods.
- The only difference is that enum constants are public, static and final (unchangeable - cannot be overriden)
- An enum cannot be used to create objects, and it cannot extend other classes(but it can implemnt interfaces).

- Why And When To Use Enums? 
- Use enums when you have values that you know aren't going to change, like month days,
  colors, deck of cards, etc.
 */ 
