// Loop through an array with for loop 
// Use length property to specify how many times you should loop 

public class Main { 
    public static void main(String[] args) {  
        String[] cars = {"Benz", "BMW", "Ford", "Volks"};
        for(int i = 0; i < cars.length; i++) { 
            System.out.println("This is: " + cars[i]);
        }
    }
}



